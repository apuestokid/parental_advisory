export const setHighchartsLocale = (locale, Highcharts) => {
  const localeConfig = highchartsLocales[locale] || highchartsLocales.es

  Highcharts.setOptions({
    lang: {
      months: localeConfig.months,
      shortMonths: localeConfig.shortMonths,
      weekdays: localeConfig.weekdays,
      shortWeekdays: localeConfig.shortWeekdays,
      thousandsSep: localeConfig.thousandsSep,
      decimalPoint: localeConfig.decimalPoint,
      loading: localeConfig.loading,
      noData: localeConfig.noData,
    },
  })
}
